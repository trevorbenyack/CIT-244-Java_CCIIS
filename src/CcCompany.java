public class CcCompany {
    private int routingNum;
    private int branchNum;
    private String companyName;
    private String branchAddress;
    private String branchCity;
    private String branchState;
    private String branchZip;
    private String branchEmail;
    private int branchPhone;

    public CcCompany() {
    }

    public int getRoutingNum() {
        return routingNum;
    }

    public void setRoutingNum(int routingNum) {
        this.routingNum = routingNum;
    }

    public int getBranchNum() {
        return branchNum;
    }

    public void setBranchNum(int branchNum) {
        this.branchNum = branchNum;
    }

    public String getbranchName() {
        return companyName;
    }

    public void setbranchName(String branchName) {
        this.companyName = branchName;
    }

    public String getbranchAddress() {
        return branchAddress;
    }

    public void setbranchAddress(String branchAddress) {
        this.branchAddress = branchAddress;
    }

    public String getbranchCity() {
        return branchCity;
    }

    public void setbranchCity(String branchCity) {
        this.branchCity = branchCity;
    }

    public String getbranchState() {
        return branchState;
    }

    public void setbranchState(String branchState) {
        this.branchState = branchState;
    }

    public String getbranchZip() {
        return branchZip;
    }

    public void setbranchZip(String branchZip) {
        this.branchZip = branchZip;
    }

    public String getbranchEmail() {
        return branchEmail;
    }

    public void setbranchEmail(String branchEmail) {
        this.branchEmail = branchEmail;
    }

    public int getbranchPhone() {
        return branchPhone;
    }

    public void setbranchPhone(int branchPhone) {
        this.branchPhone = branchPhone;
    }


}
